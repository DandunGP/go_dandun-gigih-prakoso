package calculate

func Addition(numberOne, numberTwo int) int {
	result := numberOne + numberTwo
	return result
}

func Subtraction(numberOne, numberTwo int) int {
	result := numberOne - numberTwo
	return result
}

func Division(numberOne, numberTwo int) int {
	result := numberOne / numberTwo
	return result
}

func Multiplication(numberOne, numberTwo int) int {
	result := numberOne * numberTwo
	return result
}
